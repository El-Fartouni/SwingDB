        try {
            // 1. Récupération de la saisie
            String name = nameField.getText();
            String firstName = firstNameField.getText();
            char sex = buttonGroupSex.getSelection().getActionCommand().charAt(0);

            //2a. Instanciation de l'objet Student (DTO)
            Student entity = new Student();

            //2b. Hydratation de l'objet Student (DTO)
            entity.setName(name);
            entity.setFirstName(firstName);
            entity.setSex(sex);

            //Mise à jour de la liste
            model.addElement(entity);

            //3. Instanciation du DAO
            StudentDAO dao = new StudentDAO(DatabaseConnection.getInstance());
            //Persistance de DTO
            dao.save(entity);
        } catch (SQLException ex) {
            Logger.getLogger(StudentInternalFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

