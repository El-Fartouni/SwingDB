    private void initStudentsList() {

        //Instanciation d'un nouvel objet modell
        model = new DefaultListModel();

        try {
            //Instanciation du DAO
            StudentDAO dao = new StudentDAO(DatabaseConnection.getInstance());

            //Récupération de la liste des étudiants
            List<Student> data = dao.findAll().getAll();

            //Remplissage du model à partir de la quête
            for (Student student : data) {
                model.addElement(student);
            }

            //Liaison entre le model et la liste
            studentList.setModel(model);

        } catch (SQLException ex) {
            Logger.getLogger(StudentInternalFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

